import { NextResponse } from 'next/server';
import { supabaseServer } from '../../../utils/supabaseServer';

export async function POST() {
  try {
    console.log('üîÑ Manually pausing competition...');
    
    // Pause the quiz competition
    const { error: pauseError } = await supabaseServer
      .from('quiz_competition_status')
      .upsert({ 
        status: 'paused',
        updated_at: new Date().toISOString()
      });
    
    if (pauseError) {
      console.error('‚ùå Error pausing quiz competition:', pauseError);
      throw pauseError;
    }
    
    // Also update the competition_active setting
    const { error: settingError } = await supabaseServer
      .from('quiz_settings')
      .update({ setting_value: 'false' })
      .eq('setting_key', 'competition_active');
    
    if (settingError) {
      console.error('‚ùå Error updating competition setting:', settingError);
      throw settingError;
    }

    console.log('‚úÖ Competition manually paused successfully');

    return NextResponse.json({
      success: true,
      message: 'Competition has been paused',
      status: 'paused'
    });
    
  } catch (error) {
    console.error('‚ùå API: Error pausing competition:', error);
    
    return NextResponse.json({
      success: false,
      error: 'Failed to pause competition',
      message: error.message
    }, { status: 500 });
  }
}
